    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String nombre = JOptionPane.showInputDialog("Inserte el nombre del héroe que desea eliminar");

        if (nombre == null || nombre.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No ingresaste ningún nombre.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<Heroe> lista = LogicaHeroes.getListaClientes();
        Heroe heroeAEliminar = null;

        // Buscar el héroe por nombre
        for (Heroe h : lista) {
            if (h.getNombre().equalsIgnoreCase(nombre.trim())) {
                heroeAEliminar = h;
                break; // dejamos de buscar cuando lo encontramos
            }
        }

        // Si se encontró, eliminarlo
        if (heroeAEliminar != null) {
            
  int respuesta = JOptionPane.showConfirmDialog(
        this,
        "Seguro?",
        "",
        JOptionPane.YES_NO_OPTION  // Fixed: YSS_NO_OPTION → YES_NO_OPTION
    );
  
  
  
  if (respuesta == JOptionPane.YES_OPTION){
            
            
            lista.remove(heroeAEliminar);
            JOptionPane.showMessageDialog(this, "Héroe '" + nombre + "' eliminado correctamente.");
            refrescarTabla(); // actualizar la tabla visual
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningún héroe con el nombre '" + nombre + "'.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

