    private void refrescarTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // limpia las filas sin recrear el modelo

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");

        for (Heroe heroe : LogicaHeroes.getListaClientes()) {
            model.addRow(new Object[]{
                heroe.getNombre(),
                heroe.getPoder(),
                heroe.getNivel(),
                sdf.format(heroe.getFecha())

            });
        }

        // Mantener el orden tras actualizar los datos
        sorter.sort();
    }

