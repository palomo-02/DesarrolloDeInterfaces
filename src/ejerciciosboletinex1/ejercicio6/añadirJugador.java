    private void BotonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEnviarActionPerformed

        String nombre = textoNombre1.getText().trim();
        int puntuacion = Integer.parseInt(textoPuntuacion1.getText().trim());
        int nivel = Integer.parseInt(TextoNivel.getText().trim());

        // Create the Jugador object
        Jugador jugador = new Jugador(nombre, puntuacion, nivel);

        LogicaJugadores.añadirJugador(jugador);

        // Optional: log or use the Jugador object
        System.out.println("Jugador creado: " + jugador);
        jframeTablaPuntajes parentFrame = (jframeTablaPuntajes) this.getParent();
        parentFrame.alta(jugador);
        // Close the dialog
        dispose();
    }//GEN-LAST:event_BotonEnviarActionPerformed

